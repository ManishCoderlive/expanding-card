
function showimg(element){
    if( element.style.width="6%"){
        document.querySelector('.card1').style.width="6%"
        document.querySelector('.card2').style.width="6%"
        document.querySelector('.card3').style.width="6%"
        document.querySelector('.card4').style.width="6%"
        document.querySelector('.card5').style.width="6%"
        element.style.width="50%";
       }
       else{
        document.querySelector('.card1').style.width="6%"
        document.querySelector('.card2').style.width="6%"
        document.querySelector('.card3').style.width="6%"
        document.querySelector('.card4').style.width="6%"
        document.querySelector('.card5').style.width="6%"
       }
}
